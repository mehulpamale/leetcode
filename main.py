class Solution(object):

    def letterCombinations(self, digits):
        """
        :type digits: str
        :rtype: List[str]
        """
        if not digits:
            return []
        mmap = {
            '2': 'abc',
            '3': 'def',
            '4': 'ghi',
            '5': 'jkl',
            '6': 'mno',
            '7': 'pqrs',
            '8': 'tuv',
            '9': 'wxyz',
        }

        combinations = []

        def backtrack(combination, digits):
            if len(digits) == 0:
                combinations.append(combination)
                return

            for letter in mmap[digits[0]]:
                backtrack(combination + letter, digits[1:])

        backtrack("", digits)
        return combinations


